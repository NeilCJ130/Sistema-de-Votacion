    private TIPOVOTO obtenerTipoVoto(String tipoVotoStr) {
        switch (tipoVotoStr) {
            case "Preferencial":
                return TIPOVOTO.VALIDO;
            case "Blanco":
                return TIPOVOTO.EN_BLANCO;
            case "Nulo":
                return TIPOVOTO.NULO;
            default:
                throw new IllegalArgumentException("Tipo de voto no reconocido");
        }
    }

    

