    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
 try {
        
        if (sistema.haVotado(participanteActual)) {
            JOptionPane.showMessageDialog(this, "Ya has emitido tu voto.", "Advertencia", JOptionPane.WARNING_MESSAGE);
            return; 
        }

   
        int indiceCandidato = cboCandidato.getSelectedIndex();
        int indiceMesa = cboMesa.getSelectedIndex();
        String tipoVotoStr = (String) cmbTipoVoto.getSelectedItem();

        if (indiceCandidato == -1 || indiceMesa == -1 || tipoVotoStr == null) {
            JOptionPane.showMessageDialog(this, "Todos los campos son obligatorios.", "Error", JOptionPane.ERROR_MESSAGE);
            return; 
        }

        Candidato candidatoSeleccionado = listaCandidatos.iesimo(indiceCandidato + 1); 
        MesaElectoral mesaSeleccionada = mesas.iesimo(indiceMesa + 1);
        TIPOVOTO tipoVoto = obtenerTipoVoto(tipoVotoStr);

        
        sistema.crearVoto(candidatoSeleccionado, mesaSeleccionada, tipoVoto, participanteActual);

        JOptionPane.showMessageDialog(this, "¡Tu voto ha sido registrado exitosamente!", "Éxito", JOptionPane.INFORMATION_MESSAGE);

        
        this.dispose(); 

    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error al registrar el voto: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_btnGuardarActionPerformed

