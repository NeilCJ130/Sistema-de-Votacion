    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        
      
        String nombres = this.txtNombres.getText();
        String Apellido = this.txtApellidos.getText();
        String dni = (this.txtDNI.getText());
        String PartidoPolitico = this.txtPP.getText();
        
           
       
       Candidato oCandidato = new Candidato(nombres,Apellido,dni,PartidoPolitico,"","");
          
        boolean resultado = this.sistema.agregar(oCandidato);
        if (resultado==true){
            JOptionPane.showMessageDialog(null, "Grabado correctamente!!");
        }else{
            JOptionPane.showMessageDialog(null, "Problema al guardar los datos!!");
        }    
       
        
        
      limpiarFormulario() ;
         
     
       
    
    }//GEN-LAST:event_btnGuardarActionPerformed

