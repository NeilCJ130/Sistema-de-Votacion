    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
       
    
    String nombre = this.txtNombre.getText();
    String apellido = this.txtApellido.getText();       
    String tipo = this.cboTipo.getSelectedItem().toString();
    String claveAcceso = this.txtclave.getText();
           
 
    if (nombre.isEmpty() || apellido.isEmpty() || claveAcceso.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Por favor, complete todos los campos.", "Error", JOptionPane.ERROR_MESSAGE);
        return;
    }

   
    MiembroDeMesa oMiembrodemesa = new MiembroDeMesa(nombre, apellido, tipo, claveAcceso, "", "", "");
    boolean resultado = this.sistema.agregar(oMiembrodemesa);

    if (resultado) {
        JOptionPane.showMessageDialog(this, "Miembro de mesa grabado correctamente!");

        
        
    } else {
        JOptionPane.showMessageDialog(this, "Problema al guardar los datos.", "Error", JOptionPane.ERROR_MESSAGE);
    }

    limpiarFormulario(); 



    }//GEN-LAST:event_btnGuardarActionPerformed

